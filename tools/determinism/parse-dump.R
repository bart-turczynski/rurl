write_ascii_csv <- function(df, path) {
  bad <- vapply(df, function(col) any(grepl("[^\x20-\x7e]", col)), logical(1))
  if (any(bad)) {
    stop("refusing to write non-ASCII column(s): ",
         toString(names(df)[bad]), " -> ", path)
  }
  con <- file(path, open = "wb")        # binary: force LF, never CRLF
  on.exit(close(con))
  utils::write.csv(df, con, row.names = FALSE, quote = TRUE, eol = "\n")
  invisible(path)
}

